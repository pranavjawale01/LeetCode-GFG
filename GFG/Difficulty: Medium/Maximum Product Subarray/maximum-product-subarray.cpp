class Solution {
  public:
    // Function to find maximum product subarray
    int maxProduct(vector<int> &arr) {
        // Your Code Here
        int ans = arr[0], maxPro = arr[0], minPro = arr[0];
        int n = arr.size();
        for (int i = 1; i < n; i++) {
            if (arr[i] < 0) swap(maxPro, minPro);
            maxPro = max(arr[i], arr[i] * maxPro);
            minPro = min(arr[i], arr[i] * minPro);
            ans = max(ans, maxPro);
        }
        return ans;
    }
};

